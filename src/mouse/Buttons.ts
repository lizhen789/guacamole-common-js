enum Buttons {


  /**
   * The name of the {@link GuacamoleMouseState} property representing the
   * left mouse button.
   *
   * @constant
   * @type {!string}
   */
  LEFT = 'left',

  /**
   * The name of the {@link GuacamoleMouseState} property representing the
   * middle mouse button.
   *
   * @constant
   * @type {!string}
   */
  MIDDLE = 'middle',

  /**
   * The name of the {@link GuacamoleMouseState} property representing the
   * right mouse button.
   *
   * @constant
   * @type {!string}
   */
  RIGHT = 'right',

  /**
   * The name of the {@link GuacamoleMouseState} property representing the
   * up mouse button (the fourth mouse button, clicked when the mouse scroll
   * wheel is scrolled up).
   *
   * @constant
   * @type {!string}
   */
  UP = 'up',

  /**
   * The name of the {@link GuacamoleMouseState} property representing the
   * down mouse button (the fifth mouse button, clicked when the mouse scroll
   * wheel is scrolled up).
   *
   * @constant
   * @type {!string}
   */
  DOWN = 'down'
}

export default Buttons